            Driving Safely
        </a>
        <div class="modal fade bd-example-modal-lg8" tabindex="-1" role="dialog" aria-labelledby="myLargeModalLabel" aria-hidden="true">
            <div class="modal-dialog modal-lg">
                <div class="modal-content">
                    <div class="container">
                        <h1>Driving Safely</h1>
                        <ul>
                            <li>Keep your car in good running condition. Make sure there's enough gas to get where you're going and back.</li>
                            <li>Turn the ignition off and take your car keys with you, even if you just have to run inside for one minute.</li>
                            <li>Roll up the windows and lock car doors, even if you're coming right back. Check inside and out before getting in.</li>
                            <li>Avoid parking in isolated areas. If you are uncomfortable, ask a security guard or store staff to watch you or escort you to your car.</li>
                            <li>Drive to the nearest gas station, open business, or other well-lighted, crowded area to get help if you think you are being followed. Don't head home.</li>
                            <li>Use your cellular phone, if you have one, to call the police if you are being followed or you've seen an accident. Otherwise, stay off your cellular phone while you are driving.</li>
                            <li>Don't pick up hitchhikers. Don't hitchhike.</li>
                        </ul>
