            Identity Theft a Growing Epidemic
        </a>
        <div class="modal fade bd-example-modal-lg6" tabindex="-1" role="dialog" aria-labelledby="myLargeModalLabel" aria-hidden="true">
            <div class="modal-dialog modal-lg">
                <div class="modal-content">
                    <div class="container">
                        <h1>Identity Theft a Growing Epidemic</h1>
                        <p>Identity theft occurs when someone wrongfully uses your personal identification to obtain credit, loans, services, even rentals and mortgages in your name. Information can be stolen from a consumer in a variety of ways including: Going through your mail or trash to take pre-approved credit card offers, discarded receipts or other personal information; stealing your purse or wallet; calling you over the phone posing as a solicitor in order to gain personal information including date of birth and social security number or looking over your shoulder at an ATM while you are accessing your account, to steal your password, or pin.</p>
                        <p>Avoid becoming a victim: Order copies of your credit report from all 3 credit reporting agencies to get as much information as possible. Contact the credit reporting agencies in writing and let them know you are disputing inaccurate items in your credit report due to possible identity theft. Contact the credit card companies who are reporting the false debt and have them investigate possible identity theft. Keep all copies of all letters; document phone calls and request a letter from the agencies stating these items are under investigation. Do not carry important documents such as your social security card, birth certificate or passport unless absolutely necessary.</p>
                        <p>Only carry the credit cards in your purse or wallet that you need when shopping. Keep a list of your credit card account numbers, with expiration dates and telephone numbers for all your bank accounts and keep them in a safe location. In an emergency, you can notify these companies quickly to avoid fraudulent charges or purchases. Invest in a shredder and shred discarded papers with your information. Use a mix of letters and numbers when creating passwords.</p>
                        <h4>Here are some effective ways to combat Identity Theft:</h4>
                        <ul>
                            <li>Get a FREE annual credit report: www.annualcreditreport.com. Check and review your credit report. Report any discrepancies to the credit reporting agency. Put an alert on your credit if necessary.</li>
                            <li>Stop tele-marketing calls: www.donotcall.gov</li>
                            <li>Stop receiving unwanted catalogs and other telemarketing mail: www.dmachoice.org</li>
                            <li>Shred your old credit card, bank statements, bills, etc. to avoid dumpster diving. For more information on ID Theft or what to do if you are a victim, please go to www.ftc.gov</li>
                        </ul>
                    </div>
