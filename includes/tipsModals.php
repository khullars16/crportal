                        <h1>Pickpocketing</h1>
                        <p>According to the NYPD, pickpocketing incidents increase during the months of August, September and October, obviously during the start of the school year. In the majority of the reported incidents, the victim had their wallet removed from their backpack front compartment, a common location where students put their wallets. Carry your bag in front of you. Gentlemen, carry your wallet in your front pocket.</p>
                        <h4>Common locations where thieves target victims:</h4>
                        <ul>
                            <li>By the subway turnstiles – Thief one walks in front of you swiping their metro card but they use a card that denies them access while the second thief is behind you, bumps into you removing your property (cell phone, wallet, etc)</li>
                            <li>In a crowded train – removing your wallet from your backpack front compartment.</li>
                            <li>Going up or down the stairs of the subway – removing your property from your backpack front compartment.</li>
                        </ul>
                        <p>Pickpockets thieves look like you and I and usually work in a team of two or three.</p>
