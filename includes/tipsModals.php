                        <h1>Home Safety</h1>
                        <p>Have your keys ready before you get to the door. Look back before entering your building or home. If you live in an apartment, close the lobby door behind you, especially if a stranger is approaching. Do not hold the door open for strangers. Make all visitors and delivery persons use the doorbell.</p>
