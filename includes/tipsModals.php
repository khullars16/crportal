        <a href="#" class="list-group-item list-group-item-action" data-toggle="modal" data-target=".bd-example-modal-lg9">
            Taking Buses and Subways
        </a>
        <div class="modal fade bd-example-modal-lg9" tabindex="-1" role="dialog" aria-labelledby="myLargeModalLabel" aria-hidden="true">
            <div class="modal-dialog modal-lg">
                <div class="modal-content">
                    <div class="container">
                        <h1>Taking Buses and Subways</h1>
                        <ul>
                            <li>Plan your trip before leaving. Visit the MTA website for directions or any delays in service: www.mta.info or for taxi fare, train or city bus directions www.hopstop.com</li>
                            <li>As a general precaution, whether you're in the subway, the bus, or even in the street, appear confident. Always look as if you know where you're going, and you're better off not displaying money in public. Always carry your wallet in your front pocket and carry your bag (pocket book, book bag) in front of you to avoid being a victim of a pickpocket.</li>
                            <li>
                                Do not display electronics or jewelry.</li>
                            <li>Avoid standing at the end of subway platforms or on an empty platform. Instead, wait in the Off-Hours Waiting Area, particularly at night. Most stations have one, generally located on the mezzanine level, near a station booth. Speak to the station agent or other NYC Transit employees (who wear bright orange vests) if you have a problem. Use a Customer Assistance Intercom (mounted on a platform column) to get help in a station where you're not visible to the station agent. When you speak into the Customer Assistance Intercom, the agent can speak with you. You can also use a public phone on the mezzanine or platform to dial 911 (the police) if you need help. This call is free. Each station booth posts the district and phone number of the NYPD unit that patrols the station. When it's not an emergency, use this number to contact the police. Electronic signs in many Off-Hours Waiting Areas indicate when a train is approaching the station. If you wait near the sign, you will have enough time to walk to the platform as the train arrives.</li>
                            <li>During late hours it recommended to stay in the first or middle subway car where the conductor or the motor person has a radio to alert the police if necessary.</li>
                            <li>Use well-lighted, busy stops. If you must get off at a little-used stop, try to arrange for a friend or an adult to meet you.</li>
                            <li>Bus customers who travel between 10 p.m. and 5 a.m. can Request-a-Stop. Ask the bus operator to let you off anywhere along the route, even if it isn't a designated stop. The bus operator will comply as long as he or she thinks it's a safe location. This bus will still make all regularly scheduled stops.</li>
                            <li>Watch who gets off your stop with you. If you feel uneasy, walk directly to a place where there are other people.</li>
                        </ul>
