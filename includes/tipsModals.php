            Using Electronics - MP3 Player & Cell Phones
        </a>
        <div class="modal fade bd-example-modal-lg7" tabindex="-1" role="dialog" aria-labelledby="myLargeModalLabel" aria-hidden="true">
            <div class="modal-dialog modal-lg">
                <div class="modal-content">
                    <div class="container">
                        <h1>Using Electronics - MP3 Player & Cell Phones</h1>
                        <p>According to the New York City Police Department, the snatching of electronic devices is on the rise across the city and in many other large cities around the world. This is due to both the increased value and ubiquity of these devices.</p>
                        <p>A common element in these incidents is that the victims are not paying attention to their surroundings, distracted by the device in question. Quite often the perpetrator follows the victim from behind and snatches the electronic device right out of his or her hand. In a number of these incidents, the perpetrators were riding bicycles when they targeted their victim. These events are not limited to the streets and have also occurred on subways and buses. The NYPD & FBI are currently investigating fencing operations (stores) that are buying these stolen electronic devices.</p>
                        <h4>To avoid being a victim:</h4>
                        <ul>
                            <li>Pay attention to your surroundings, minimize the amount of time you talk on your cell phone in public places.</li>
                            <li>If you suspect you are being followed, go into an open store or a Public Safety Safe Haven and have the clerk call the Police or Public Safety right-away.</li>
                            <li>Keep all electronic devices in an inside jacket pocket. Avoid clipping them onto outer garments or displaying them anywhere visible.</li>
                            <li>If your cell phone is stolen, call your carrier and report the theft. Request that an alert be placed on the phone, preventing its use.</li>
                            <li>Mark your device by engraving (Operation ID) or with invisible ink (Operation Blue Light). These programs are offered free of charge by Public Safety. For more Information, please call 854-8513.</li>
                        </ul>
