                        <ul>
                            <li>Be AWARE of your surroundings at all times and trust your INSTINCTS.</li>
                            <li>Stay in well lit, populated pathways. Avoid shortcuts.</li>
                            <li>Travel in groups. There's always safety in numbers.</li>
                            <li>Walk with your head upright. Make eye contact. Thieves often target victims who are not paying attention to their surroundings or who are looking down.</li>
                            <li>Pay attention to your surroundings when using electronics on the streets, subways & buses. Don't TUNE yourself out. DON'T WALK AND TEXT.</li>
                            <li>Don't display electronics when not in use. Avoid becoming a victim of "Apple Picking."</li>
                            <li>Avoid traveling through parks after dark.</li>
                            <li>Before entering your apartment building, have your keys ready. Don't hold doors for anyone whom you don't know.</li>
                            <li>If you observe anyone acting in a suspicious manner, or if you feel threatened in any way, call the police immediately by dialing 911.</li>
